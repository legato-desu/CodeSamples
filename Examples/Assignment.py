# Assignment

"""
Create a program that uses the different types of assignment with examples:
= Assign
+= add and assign
-= subtract and assign
*= multiply and assign
/= divide and assign
**= raise "exponent" and assign
"""
number_1 = 10
number_1 += 5
print(f"(+=): {number_1}")

number_2 = 10
number_2 -= 5
print(f"(-=): {number_2}")

number_3 = 10
number_3 *= 5
print(f"(*=): {number_3}")

number_4 = 10
number_4 /= 5
print(f"(/=): {number_4}")

number_5 = 10
number_5 **= 2
print(f"(**=): {number_5}")