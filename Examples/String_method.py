# String method

"""
Create a program that uses different string methods to modify text
"""
# UPER --> convert everything to uppercase
city = "Bogota"
city = city.upper()
print(city)

# LOWER  --> convert everything to lowercase
country = "CANADA"
country = country.lower()
print(country)

# CAPIALIZE  --> Convert the first letter to uppercase
color = "AZUL"
color = color.capitalize()
print(color)