# Logical values

"""
Create a program that uses the different logical operators:
and
or
not
"""
# It is true if they are all true
logic_1 = 5 > 3 and 10 > 5 and 50 > 9
print(logic_1)

# It is false if they are all false
logic_2 = 1 > 3 or 10  > 5 or 50 > 9
print(logic_2)

# Gives the opposite value of the expression 
logic_3 = not 5==4
print(logic_3)