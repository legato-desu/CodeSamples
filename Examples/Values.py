# Values
import numpy as np

"""
Create a program that displays the updated values ​​of the numpy library
"""
a = np.array([(1.5, 2, 3),(4, 5, 6)])
b = np.array([1, 2, 3])

# Exponential (exp)
Exponential = np.exp(b)
print(f"Exponencial: {Exponential}")

# Square root (sqrt)
root = np.sqrt(b)
print(f"Raiz: {root}")

# Sine  (sin)
Sine = np.sin(a)
print(f"Seno: {Sine}")

# cosine (cos)
cosine =np.cos(a)
print(f"Coseno: {cosine}")

# Natural logarithm (log)
logarithm =np.log(b)
print(f"logaritmo: {logarithm}")