"""
= Asignar
+= sumar y asignar
-= resta y asigna
*= multiplica y asigna
/= divide y asigna
**= eleva "exponente" y asigna
"""
numero_1 = 10
numero_1 += 5
print(numero_1)

numero_2 = 10
numero_2 -= 5
print(numero_2)

numero_3 = 10
numero_3 *= 5
print(numero_3)

numero_4 = 10
numero_4 /= 5
print(numero_4)

numero_4 = 10
numero_4 **= 2
print(numero_4)