# Comparisons

"""
Create a program that allows you to use the different logical comparisons:
== equality
> greater than
>= greater than or equal
< smaller than
<= less than or equal
!= different
"""
example_1 = "david" == "david"
print(example_1)

example_2 = 10 > 5
print(example_2)

example_3 = 5 >= 5
print(example_3)

example_4 = 3 < 5
print(example_4)

example_5 = 4 <= 5
print(example_5)

example_6 = 1 != 5
print(example_6)